package bcci;

public class Stadium {
        private long Id;
        private String Name;
        private String City;
        
        public long getId() {
        	return Id;	
        }
	
    public void setId(long id) {
		Id = id;
	}
	
    public String getName() {
		return Name;
	}
	
    public void setName(String name) {
		Name = name;
	}
	
    public String getCity() {
		return City;
	}
	
    public void setCity(String city) {
		City = city;
	}
	
   
}
